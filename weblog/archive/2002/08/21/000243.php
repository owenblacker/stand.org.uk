		<div class="entry-body"><p>What will copyright look like in the 21st century? Many people have many
different ideas - it's all very much still in flux. The European Union,
however, decided exactly how it would be on the 22nd of May, 2001. That was
the date the <a href="http://uk.eurorights.org/issues/eucd/eucd.html">European
Directive "on the harmonisation of certain aspects of copyright and related
rights in the Information Society"</a> was passed. It will become law across
Europe by Christmas of this year at the very latest: countries failing to
follow the order will be in breach of the Treaty of Rome.  Our government are
law-abiding, so they've already drafted <a
href="http://www.patent.gov.uk/about/consultations/eccopyright">the patches
needed</a> to upgrade existing UK law. It's in its "consultation period" from
now until October.</p> 

<p> The European Copyright Directive (or EUCD) is one of a raft of new
proposals sweeping across the world that give brand new powers to copyright
holders. The reason is a fear of widespread, easy, perfect copying over the
Net. But the cure may be worse than the problem ever was: especially if you're
a humble consumer.  </p>

<p> The EUCD - and the proposed UK implementation of it - decrees that
breaking the copyright protection on a piece of media will henceforth become
as illicit as copying the file itself. Explaining how to break such systems
will become a criminal offence.</p>

<p>Thankfully, the original directive had a <a
href="http://uk.eurorights.org/issues/eucd/eucd.html#article5">few get-out
clauses</a>. <a
href="http://uk.eurorights.org/issues/eucd/eucd.html#main48">Cryptographic
researchers</a>, for instance, may defend their analysis of flaws in software
as part of the free flow of academic knowledge (rather than go straight to
jail).  Others are presumed to have special rights to access the media without
having to jump through complicated hoops to copy it: librarians, teachers, the
disabled, were examples given. Oh, and the EUCD also tries to cover all your
old abilities as a user of copyrighted material - the right to time-shift TV
programs, the right to make a personal copy - as long as it doesn't have an
"economic effect". For these reasons, and a smattering of others, are you
allowed to break copyright protection.</p>

<p> The British implementation has none of these get-outs. Or rather it does,
but they're offered in a very peculiar way. Here's how it works: </p>

<ol>
<li>Media corporations can put any restriction they like on how you access their
intellectual property.</li>
<li>If you break that restriction, they can sue you. If you tell anyone
else how to break it, you can go to jail.</li>
<li>If you want to break it because of the rights that belong to you under
copyright law, you should write to the Secretary of State personally and ask
permission.</li>
<li>Once she's said yes, you may freely use that ink marker to fix that crippled
CD so that they can finally play on your home computer.</li>
<li>But don't tell anyone else how you did it (see 3. above, re: jail).
</ol>

<p>What does this mean? As Julian Midgley of the Campaign for Digital Rights
(CD-R) says in his excellent analysis of the proposals:</p>

<blockquote>
As it stands, the UK implementation of the European Copyright Directive will hinder research into cryptography (in contravention of the express intent of the Directive itself), make criminal current common practices of the music industry, give software companies unwarranted control over the creation of software products interoperable with their own, and provides an inadequate and entirely impractical mechanism for beneficiaries of the Directive's exceptions to obtain access to copyrighted works protected by technological measures. In addition, it provides no clear mechanism for ensuring that copyrighted works protected by technological measures will eventually be released into the public domain. </blockquote>

<p>Julian has some great ideas for patching up this law. It's never going to
be a great law, but some of the worst bits are fixable. But the government is
going to need a bit of push to tidy it up.</p>

<b>So what can you do?</b>

<p>
If you think this is a bad idea, what should you do? <a
href="http://www.faxyourmp.com/">Fax your MP</a> again?
</p>

<p>
<p>Well, unfortunately, this is an EU Directive.</p> 

<p>EU Directives are
implemented using statutory instruments. Now, as you might remember from
earlier this year, it's hard, but not impossible to stop a Statutory
Instrument - that's what the Blunkett RIP proposals were, the ones that were
going to allow parish councils to look at your private telephone and Web
records. Which you stopped.</p>

<p> But that the RIP proposal was what's called a <i>positive</i> statutory
instrument. EU Directives work as <i>negative</i> statutory instruments.
Positive statutory instruments become law as soon as they receive a quick,
undiscussed vote in the Commons and Lords. Negative statutory instruments
are set to become law as soon as they are announced. They only <i>stop</i> being law if
Parliament passes a motion cancelling them within forty days. They're like
those "Book of The Month Club" things. You keep getting them until you tell
them to stop. </p>

<p>The chances of Parliament successfully voting down a positive SI is very
slim. It happens though. The chances of Parliament spontaneously getting
together, drafting a bill to undo an already existing law, getting it into the
timetable then rapturously and then unanimously voting their new law in, and
the old government law out, doesn't even happen in Jeffrey Archer novels.</p>

<b>So what can you do, really?</b>

<p>

<b><a href="http://uk.eurorights.org/issues/eucd/ukimpl/">Read the Campaign
for Digital Rights' analysis</a>.</b><br>
<b><a href="http://www.patent.gov.uk/about/consultations/eccopyright">Read the
proposal itself</a></b>.
The "Summary" bit explains how send in your comments.<br>
<b><a
href="http://www.patent.gov.uk/about/consultations/eccopyright/summary.htm">Tell
the Patent Office what you think</a></b>. The e-mail address 
is in the consultation document.</br>
</p>

<p>It's a consultation. The proposed law is broken. They should listen, and if
enough people write in, they should fix it.</p>

<p> For extra effectiveness, you could print your comments out and send a copy
to the Secretary of State, Patricia Hewitt. She is, after all, the Patent
Office's boss.</p>

<p>And if she's going to implement this, she should get used to a lot,
<i>lot</i>, more mail.</p>

