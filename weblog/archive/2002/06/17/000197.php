		<div class="entry-body"><p><p>The RIP Statutory Instrument has been <a href="http://news.bbc.co.uk/hi/english/uk_politics/newsid_2049000/2049308.stm">postponed to July 1st</a>. Rumours indicate this will give time for the Home Secretary to tack on some additional safeguards.</p> <br />
<p>It's not over yet, but at least we have time to breathe, and bring MPs up to speed on all the ramifications of these orders.</p><br />
<p>One lesson we've learnt: we need to keep a closer eye on the Parliamentary schedule. The Statutory Instrument was first placed before the House Of Commons on <i>May 20th</i>. If someone had spotted it before then, it would have only taken 20 MPs to force a parliamentary debate. As it is, the only debate on the order will be in a Standing Committee, with just 17 members.</p><br />
<p>These measures can be stopped - but what was it that man said about eternal vigilance?</p></p></div>
		
