<br>
<p>One final comment: the Prime Minister's <a href="http://politics.guardian.co.uk/homeaffairs/story/0,11026,735501,00.html
">official response</a> has been to emphasise that RIP is for "combat serious crime" only. His spokesman then goes on to list the cases in which the traffic data can be legitimately obtained.
That raises a few questions: firstly, if it's for serious crime only, why does the law permit suspicion of <i>any</i> crime to be sufficient reason(we <a href="http://www.stand.org.uk/ripnotes/#traffic">actively lobbied</a> for the inclusion of the word serious in the original Bill).
Second: the list of reasons the PM's spokesman gives is complete, except for one omission - the one that allows the Secretary of State to add any other reasons, using another Statutory Instrument. If we can't trust them to restrict the users of traffic data, how can we trust them to restrict the usage?
And third: if we can expect this law to be only used in the most serious cases, can we expect the most serious punishments for those who abuse it? A criminal trial? A prison sentence? Or just a perfunctory examination by the secretive RIP tribunal?</p>
