		<div class="entry-title">Post Office To Steam Open Your History File</div>
		<div class="entry-body"><p><p>One of the more extreme powers the Regulation of Investigatory Powers Act<br />
(RIPA) handed out two years ago let a few government agencies obtain "traffic<br />
data" without a judicial warrant.</p></p>

<p><p>Traffic data is best described as the writing on the envelope of a message,<br />
instead of its contents. It can be the list of phone numbers you have called<br />
in the last six months. Or a full list of Websites you have visited. Or the<br />
times you log on, and from where. Or who you e-mail, or what programs you've<br />
downloaded, or what newsgroups you read. Or the position of your cellphone<br />
last Tuesday at five.</p></p>

<p><p>Because the risk of abuse of this power (there's no judicial oversight -<br />
all that's needed is the permission of a suitably high-powered boss), those<br />
who could wield were strictly limited. Only the police, Customs and Excise and<br />
the secret services were allowed access to traffic data in the original<br />
act.</p></p>

<p><p>Not any more.</p></p>

<p><p>On Friday, the Home Office petitioned parliament to add a vast array of<br />
organisations to that list. If their passes, everyone from the DTI, any local<br />
authority, the Food Standards Agency, the Home Office themselves (of course),<br />
and staggeringly enough, <i>Consignia</i>.</p></p>

<p><p>If the idea that the fricking <i>Post Office</i> has access to your web<br />
logs (access which would cost a competitive company millions, and would<br />
probably get them investigated by the Data Protection people), let alone every<br />
minor apparatchik on the block, you might want to kick up a fuss about this.<br />
It's due to appear before MPs on June 18th, and the Lords a little after.</p></p>

<p><p><b>How do I find out more?</b></p></p>

<p><p>Read the <br />
<a href="http://www.legislation.hmso.gov.uk/si/si2002/draft/20022322.htm"> Orderbefore Parliament</a>. It's very short (although the list of allowed<br />
organisations is very long). (2 minutes).</p></p>

<p><p>Flick through our <a href="http://www.stand.org.uk/ripnotes/#traffic">quick<br />
notes on the original RIPA law</a>. (The notes are based on an earlier draft,<br />
so the section numbers are a bit off. But you get the idea.)</p></p>

<p><p><b>What can I do?</b></p></p>

<p><p><br />
<a href="http://www.faxyourmp.com/">Fax your MP</a> <b>now</b>. The Order is<br />
to be debated next Tuesday, and these things are usually rubber-stamped. Tell<br />
your MP which groups you don't want to be spied on by (list them all), and<br />
tell him why. Explain what traffic data means (your MP might not now how<br />
wide-ranging it is). Explain it in terms he or she can understand - if they're<br />
a Conservative, explain how it's government prying into people's lives. If<br />
they're Labour, talk about civil rights. If they're Liberal, say what you like<br />
- the LibDems are usually down with this sort of nonsense. Ask for a reply.</p></p>

<p><p><br />
It'll take twenty minutes of your time. It'll make a difference. Members of<br />
Parliament hate having this sort of wide-ranging power sneaked past them as<br />
much as you do.<br />
</p></p>

