		<div class="entry-body"><p>One of the more extreme powers the <a href="http://www.stand.org.uk/commentary.php3">Regulation of Investigatory Powers Act
</a> (RIPA) handed out two years ago let government agencies obtain "traffic
data" without a judicial warrant.</p>

<p>Traffic data is best described as the writing on the envelope of a message,
instead of its contents. It can be the list of phone numbers you have called
in the last six months. Or a full list of Websites you have visited. Or the
times you log on, and from where. Or who you e-mail, or what programs you've
downloaded, or what newsgroups you read. Or the position of your cellphone
last Tuesday at five.</p>

<p>Because the risk of abuse of this power (there's no judicial oversight -
all that's needed is the permission of a suitably high-powered boss), those
who could wield it were strictly limited. Only the police, Customs and Excise and
the secret services were allowed access to traffic data in the original
act.</p>

<p>Not any more.</p>

<p>On Friday, the Home Office petitioned parliament to add a vast array of
organisations to that list. If their passes, everyone from the DTI, any local
authority, the Food Standards Agency, the Home Office themselves (of course),
and staggeringly enough, <i><a href="http://www.consignia.com/">Consignia</a></i>. 
The final entry in the list says that 
"A Universal Service Provider within the meaning of the Postal Services Act 2000"
has the same power as the secret services to read your traffic data. There's only one USP 
in Britain right now, and that's the provider previously known as the Royal Mail.
</p>

<p>If the idea that the fricking <i>Post Office</i> has access to your web
logs (access which would cost a competitive company millions, and would
probably get them investigated by the Data Protection people), let alone every
minor apparatchik on the block, you might want to kick up a fuss about this.
It's due to appear before MPs on June 18th, and the Lords a little after.</p>

<p><b>How do I find out more?</b></p>

<p>Read the 
<a href="http://www.legislation.hmso.gov.uk/si/si2002/draft/20022322.htm">Order before Parliament</a>. It's very short (although the list of allowed
organisations is very long - two minutes should do it).</p>

<p>Flick through our <a href="http://www.stand.org.uk/ripnotes/#traffic">quick
notes on the original RIPA law</a>. (The notes are based on an earlier draft,
so the section numbers are a bit off. But you get the idea.)</p>

<p><b>What can I do?</b></p>

<p>
<a href="http://www.faxyourmp.com/">Fax your MP</a> <b>now</b>. The Order is
to be debated next Tuesday, and these things are usually rubber-stamped. Tell
your MP which groups you don't want to be spied on by (list them all), and
tell him why. Explain what traffic data means (your MP might not now how
wide-ranging it is). Explain it in terms he or she can understand - if they're
a Conservative, explain how it's government prying into people's lives. If
they're Labour, talk about civil rights. If they're Liberal, say what you like
- the LibDems are usually down with this sort of nonsense. Ask for a reply.</p>

<p>
It'll take twenty minutes of your time. It'll make a difference. Members of
Parliament hate having this sort of wide-ranging power sneaked past them as
much as you do.
</p>

