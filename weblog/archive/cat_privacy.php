			<div class="entry-title">The Blunt Instrument Postponed</div>
			<div class="entry-body"><p>First round to us: after protests by MPs, the Government
has postponed debate on RIPA Statutory Instrument  (<a href="http://www.stand.org.uk/weblog/archive/2002/06/10/000193.php">full
details here</a>). Instead of next Tuesday (18th June), the Commons
debate will now take place on the following Monday (24th June).  There's still
a strong chance that, with enough public pressure and bad publicity, the
Government can be pressured to withdraw the instrument. It's already had
strongly negative coverage on Newsnight, the BBC's Question Time, and many
other news sources.</p>

<p><a href="http://www.faxyourmp.com">Fax Your MP</a> stats are showing around
280-300 faxes sent per day. That's unusually high, and while we can't tell what those faxes
contain, we'd guess that almost all of them are related to protests against
the RIP instrument.</p>

<p>If you haven't written your letter yet, you might want to consider these
thoughts by others:

<blockquote>"the police service have had a strict RIPA manual and training procedures, and
the background of years of operating [warrants], whereas the new Public
Authorities [in the order] do not."
</blockquote>
<cite>- <a href="http://www.chiark.greenend.org.uk/pipermail/ukcrypto/2002-June/019170.html">Roland
  Perry</a>, Director of Public Policy, <a href="http://www.linx.net">London
  Internet Exchange</a></cite>

<blockquote>
"I spoke to the deputy leader of the city council who was actually quite
alarmed at the prospect of the local authority being given these powers."
</blockquote>
<cite>- <a href="http://www.barbelith.com/underground/topic.php?id=7251#post119690">journalist</a>,
  c/o <a href="http://www.barbelith.com/">Barbelith</a></cite>
<blockquote>
"The difficulty that the Government has encountered in getting the right
processes in place for the police should make us ultra-cautious in extending
these powers to such a wide range of bodies.  We don't think that there's been
enough resources put into the oversight arrangements for the current
proposals, let alone what will be needed for this huge extension. In practice,
these bodies are going to obtain this personal data on anyone they wish,
